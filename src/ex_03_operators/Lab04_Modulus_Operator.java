package ex_03_operators;

public class Lab04_Modulus_Operator {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a%b); //mod -> remainder
        // 10 /20/2 ---->quotient
        //     20
       // ------------
        //      0  -> remainder

        System.out.println(13%7);

        //any number whenever %2 if it gives "1" then its odd number if it gives "0"
        //then its even number
        // 11%2 -> 1 remainder
        //10 %2 -> 0 remainder
    }
}
